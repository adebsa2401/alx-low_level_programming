#include "3-calc.h"

/**
 * op_add - op add
 * @a: int
 * @b: int
 *
 * Return: int
 */
int op_add(int a, int b)
{
return (a + b);
}

/**
 * op_sub - op sub
 * @a: int
 * @b: int
 *
 * Return: int
 */
int op_sub(int a, int b)
{
return (a - b);
}

/**
 * op_mul - op mul
 * @a: int
 * @b: int
 *
 * Return: int
 */
int op_mul(int a, int b)
{
return (a * b);
}

/**
 * op_div - op div
 * @a: int
 * @b: int
 *
 * Return: int
 */
int op_div(int a, int b)
{
return (a / b);
}

/**
 * op_mod - op mod
 * @a: int
 * @b: int
 *
 * Return: int
 */
int op_mod(int a, int b)
{
return (a % b);
}
